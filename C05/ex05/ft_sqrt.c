int	ft_sqrt(int nb)
{
	int	c;
	int	square;

	square = 0;
	c = 0;
	while (nb > square)
	{
		square = (c + 1) * (c + 1);
		c++;
	}
	if (nb < square)
		return (0);
	else
		return (c);
}
